package parser;

public class Action {

    public String message;
    public int intValue;
    public char charValue;

    public Action() {
    }

    public void setMessage(String _message) {
        message = _message;
    }

    public void setIntValue(int _intValue) {
        intValue = _intValue;
    }

    public void setCharValue(char _charValue) {
        charValue = _charValue;
    }
}
